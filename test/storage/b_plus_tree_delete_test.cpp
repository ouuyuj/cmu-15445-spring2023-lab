TEST(BPlusTreeTests, DeleteTest3) {
  // create KeyComparator and index schema
  auto key_schema = ParseCreateStatement("a bigint");
  GenericComparator<8> comparator(key_schema.get());

  auto disk_manager = std::make_unique<DiskManagerUnlimitedMemory>();
  auto *bpm = new BufferPoolManager(50, disk_manager.get());
  // create and fetch header_page
  page_id_t page_id;
  auto header_page = bpm->NewPage(&page_id);
  // create b+ tree
  BPlusTree<GenericKey<8>, RID, GenericComparator<8>> tree("foo_pk", header_page->GetPageId(), bpm, comparator, 3, 4);
  GenericKey<8> index_key;
  RID rid;
  // create transaction
  auto *transaction = new Transaction(0);

  for (int64_t key = 1; key < 40; key += 2) {
    int64_t value = key & 0xFFFFFFFF;
    rid.Set(static_cast<int32_t>(key >> 32), value);
    index_key.SetFromInteger(key);
    tree.Insert(index_key, rid, transaction);
    // tree.Print(bpm);
    std::cout << tree.DrawBPlusTree() << std::endl;
    std::cout << "<===================>" << std::endl;
  }

  for (int64_t key = 38; key >= 1; key -= 2) {
    int64_t value = key & 0xFFFFFFFF;
    rid.Set(static_cast<int32_t>(key >> 32), value);
    index_key.SetFromInteger(key);
    tree.Insert(index_key, rid, transaction);
    // tree.Print(bpm);
    std::cout << tree.DrawBPlusTree() << std::endl;
    std::cout << "<===================>" << std::endl;
  }

  std::vector<RID> rids;
  for (int64_t key = 1; key < 40; key++) {
    rids.clear();
    index_key.SetFromInteger(key);
    tree.GetValue(index_key, &rids);
    EXPECT_EQ(rids.size(), 1);

    int64_t value = key & 0xFFFFFFFF;
    EXPECT_EQ(rids[0].GetSlotNum(), value);
  }

  int64_t current_key = 1;
  for (auto iterator = tree.Begin(); iterator != tree.End(); ++iterator) {
    auto location = (*iterator).second;
    EXPECT_EQ(location.GetPageId(), 0);
    EXPECT_EQ(location.GetSlotNum(), current_key);
    current_key = current_key + 1;
  }

  // tree.Print(bpm);
  std::cout << tree.DrawBPlusTree() << std::endl;
  std::cout << "<===================>" << std::endl;

  std::vector<int64_t> remove_keys = {37, 2, 13, 25, 17, 26, 29, 35, 38, 27, 39, 36, 9,  24, 16, 33,
                                      31, 5, 4,  18, 19, 1,  3,  20, 32, 18, 19, 6,  7,  20, 34, 6,
                                      1,  3, 10, 11, 18, 19, 27, 8,  12, 22, 21, 28, 20, 14, 30, 15};
  for (auto key : remove_keys) {
    index_key.SetFromInteger(key);
    tree.Remove(index_key, transaction);

    // tree.Print(bpm);
    std::cout << "Remove(" << key << "):" << std::endl;
    std::cout << tree.DrawBPlusTree() << std::endl;
    std::cout << "<===================>" << std::endl;
  }
  int64_t key = 13;
  index_key.SetFromInteger(key);
  tree.Remove(index_key, transaction);
  tree.Print(bpm);
  std::cout << tree.DrawBPlusTree() << std::endl;

  int64_t value = key & 0xFFFFFFFF;
  rid.Set(static_cast<int32_t>(key >> 32), value);
  index_key.SetFromInteger(key);
  tree.Insert(index_key, rid, transaction);

  // tree.Print(bpm);
  // std::cout << "Remove(" << key << "):" << std::endl;
  std::cout << tree.DrawBPlusTree() << std::endl;
  // std::cout << "<===================>" << std::endl;

  int64_t size = 0;
  bool is_present;

  for (int64_t key = 1; key < 40; key++) {
    rids.clear();
    index_key.SetFromInteger(key);
    is_present = tree.GetValue(index_key, &rids);

    if (!is_present) {
      EXPECT_NE(std::find(remove_keys.begin(), remove_keys.end(), key), remove_keys.end());
    } else {
      EXPECT_EQ(rids.size(), 1);
      EXPECT_EQ(rids[0].GetPageId(), 0);
      EXPECT_EQ(rids[0].GetSlotNum(), key);
      size = size + 1;
    }
  }

  EXPECT_EQ(size, 1);

  bpm->UnpinPage(HEADER_PAGE_ID, true);
  delete transaction;
  delete bpm;
}
